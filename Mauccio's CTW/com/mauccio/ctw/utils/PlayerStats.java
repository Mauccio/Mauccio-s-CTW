package com.mauccio.ctw.utils;

public class PlayerStats {
    public final int score;
    public final int kills;
    public final int deaths;
    public final int wools;

    public PlayerStats(int score, int kills, int deaths, int wools) {
        this.score = score;
        this.kills = kills;
        this.deaths = deaths;
        this.wools = wools;
    }
}
